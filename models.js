class Student {
    constructor(id, nombre, apellidos, edad) {
        this.id = id
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
    }
}
class Grupos {
    constructor(id, nombre) {
        this.id = id
        this.nombre = nombre   
    }
}

class Materia {
    constructor(id, nombre, nota) {
        this.id = id
        this.nombre = nombre   
        this.nota = nota
    }
}
